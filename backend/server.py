async def generate_nutrient_analysis(responses):
    content1 = "Here is a list of food items and their nutrient information:\n" \
           f"{responses}\n\n" \
           "Briefly summarize the key nutritional strengths and weaknesses of these food items, and compare their nutrient profile to the USDA dietary guidelines for a balanced diet. " \
           "Focus on the most important nutrients and areas where the food items align with or deviate from the guidelines. Limit your response to 75 words."

    response1 = client.chat.completions.create(
        model="gpt-4",
        messages=[
            {
                "role": "system",
                "content": "You are a knowledgeable nutrition expert. Provide a focused nutrient analysis and comparison to USDA guidelines based on the given context."
            },
            {
                "role": "user",
                "content": content1
            }
        ]
    )

    focused_analysis_usda_comparison = response1.choices[0].message.content

    # Step 2: Ask for concise and actionable suggestions
    content2 = f"Based on the following analysis and comparison:\n{focused_analysis_usda_comparison}\n\n" \
              "Provide 1-2 concise and actionable suggestions to improve the nutritional balance of these food items. " \
              "Focus on the most important changes that can be easily implemented. Limit your response to 40 words."

    response2 = client.chat.completions.create(
        model="gpt-4",
        messages=[
            {
                "role": "system",
                "content": "You are a knowledgeable nutrition expert. Provide concise and actionable suggestions for improving nutritional balance."
            },
            {
                "role": "user",
                "content": content2
            }
        ]
    )

    actionable_suggestions = response2.choices[0].message.content

    # Step 3: Combine the focused analysis, USDA comparison, and actionable suggestions into a final summary
    content3 = f"Please combine the following information into a concise nutrient analysis summary for the user:\n\n" \
              f"Key Nutritional Strengths, Weaknesses, and USDA Comparison:\n{focused_analysis_usda_comparison}\n\n" \
              f"Actionable Suggestions for Improvement:\n{actionable_suggestions}\n\n" \
              "Provide a clear, easy-to-understand, and engaging summary that can be displayed on a phone. Use a friendly tone and limit your response to 75 words."

    response3 = client.chat.completions.create(
        model="gpt-4",
        messages=[
            {
                "role": "system",
                "content": "You are a knowledgeable nutrition expert. Provide a concise, user-friendly, and engaging nutrient analysis summary for display on a phone."
            },
            {
                "role": "user",
                "content": content3
            }
        ]
    )

    final_summary = response3.choices[0].message.content
    return final_summary


@app.post("/uploadImage")
async def upload_image(image: UploadFile = File(...)):
  
    # img_path = "/Users/zhengyi-xiao/Desktop/WechatIMG261.jpg"
    # image = Image.open(img_path)

    image_data = await image.read()
    image_buffer = io.BytesIO(image_data)

    image = Image.open(image_buffer)

    # save the image as png
    inputs = Segprocessor(images=image, return_tensors="pt").to(device)
    outputs = Segmodel(**inputs)
    logits = outputs.logits.cpu()

    upsampled_logits = nn.functional.interpolate(
        logits,
        size=image.size[::-1],
        mode="bilinear",
        align_corners=False,
    )

    pred_seg = upsampled_logits.argmax(dim=1)[0]

    result = {}
    for label, label_str in id2labels.items():
        cropped_image, cropped_image_with_white, score = extract_cropped_image_by_labels(
            image, pred_seg, [int(label)]
        )
        if cropped_image:
            print(f"Detected {label_str} with a score of {score}.")
            result[label_str] = score
            
    responses = []
    for label, _ in result.items():
        if label in ontology:
            responses.append(ontology[label])
    analysis = await generate_nutrient_analysis(responses)
    return JSONResponse(content={"nutrients": responses, "suggestions": analysis}, status_code=200)
  
